    add("How do I implement grid-based A* pathfinding for N64 in Pak?",
        "```pak\nconst GRID_W: i32 = 16\nconst GRID_H: i32 = 16\nconst MAX_OPEN: i32 = 64\n\n"
        "struct PathNode {\n    x: i32\n    y: i32\n    g: i32  -- cost from start\n"
        "    h: i32  -- heuristic to goal\n    parent: i32  -- index in closed list\n}\n\n"
        "static grid_blocked: [bool; 256] = undefined  -- 16x16 grid\n"
        "static open_list: [PathNode; 64] = undefined\n"
        "static open_count: i32 = 0\n\n"
        "fn heuristic(ax: i32, ay: i32, bx: i32, by: i32) -> i32 {\n"
        "    let dx: i32 = ax - bx\n    let dy: i32 = ay - by\n"
        "    if dx < 0 { dx = -dx }\n    if dy < 0 { dy = -dy }\n"
        "    return dx + dy  -- Manhattan distance\n}\n\n"
        "fn is_blocked(x: i32, y: i32) -> bool {\n"
        "    if x < 0 or x >= GRID_W or y < 0 or y >= GRID_H {\n"
        "        return true\n    }\n"
        "    return grid_blocked[y * GRID_W + x]\n}\n\n"
        "fn find_path(sx: i32, sy: i32, gx: i32, gy: i32,\n"
        "             out_x: *[i32; 32], out_y: *[i32; 32]) -> i32 {\n"
        "    -- simplified A* that returns step count\n"
        "    -- for N64: limit iterations to stay within frame budget\n"
        "    open_count = 0\n"
        "    let MAX_ITERS: i32 = 32  -- never run more than 32 iterations\n"
        "    let iters: i32 = 0\n\n"
        "    -- add start node\n"
        "    open_list[0].x = sx\n    open_list[0].y = sy\n"
        "    open_list[0].g = 0\n"
        "    open_list[0].h = heuristic(sx, sy, gx, gy)\n"
        "    open_list[0].parent = -1\n    open_count = 1\n\n"
        "    loop {\n        if open_count == 0 or iters >= MAX_ITERS { break }\n"
        "        -- find lowest f = g + h\n"
        "        let best: i32 = 0\n        let bi: i32 = 1\n"
        "        loop {\n            if bi >= open_count { break }\n"
        "            if open_list[bi].g + open_list[bi].h <\n"
        "               open_list[best].g + open_list[best].h {\n"
        "                best = bi\n            }\n            bi = bi + 1\n        }\n\n"
        "        let cur: PathNode = open_list[best]\n"
        "        if cur.x == gx and cur.y == gy {\n"
        "            -- found path\n            return iters\n"
        "        }\n"
        "        -- remove best from open, expand neighbors\n"
        "        open_list[best] = open_list[open_count - 1]\n"
        "        open_count = open_count - 1\n        iters = iters + 1\n    }\n"
        "    return 0\n}\n```\n\n"
        "**Performance note**: Run A* over multiple frames for distant enemies. "
        "Store the path and follow it — don't recalculate every frame.")

