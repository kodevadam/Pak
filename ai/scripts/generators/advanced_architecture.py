    add("How do I architect a robust save system for N64?",
        "```pak\nuse n64.eeprom\n\n"
        "const SAVE_MAGIC: u32 = 0x504B5356  -- 'PKSV'\n"
        "const SAVE_VERSION: u8 = 1\n"
        "const SAVE_SLOT_SIZE: i32 = 64  -- 64 bytes per slot = 8 EEPROM blocks\n\n"
        "struct SaveSlot {\n"
        "    magic: u32          -- 4 bytes\n"
        "    version: u8         -- 1 byte\n"
        "    slot_index: u8      -- 1 byte\n"
        "    checksum: u16       -- 2 bytes\n"
        "    level: u8           -- 1 byte\n    lives: u8           -- 1 byte\n"
        "    score: i32          -- 4 bytes\n"
        "    flags: u32          -- 4 bytes (bitfield for collected items)\n"
        "    -- total: 18 bytes used, pad to 64\n    pad: [u8; 46]\n}\n\n"
        "fn save_checksum(slot: *SaveSlot) -> u16 {\n"
        "    let sum: u32 = 0\n"
        "    let ptr: *u8 = slot as *u8\n"
        "    let i: i32 = 0\n"
        "    loop {\n        if i >= sizeof(SaveSlot) as i32 { break }\n"
        "        sum = sum + (ptr[i] as u32)\n        i = i + 1\n    }\n"
        "    return (sum & 0xFFFF) as u16\n}\n\n"
        "fn save_write(slot_idx: i32, data: *SaveSlot) -> bool {\n"
        "    if not eeprom.present() { return false }\n"
        "    data.magic = SAVE_MAGIC\n    data.version = SAVE_VERSION\n"
        "    data.slot_index = slot_idx as u8\n    data.checksum = 0\n"
        "    data.checksum = save_checksum(data)\n\n"
        "    -- each EEPROM block = 8 bytes, slot = 8 blocks\n"
        "    let block_start: i32 = slot_idx * 8\n"
        "    let ptr: *u8 = data as *u8\n    let b: i32 = 0\n"
        "    loop {\n        if b >= 8 { break }\n"
        "        eeprom.write(block_start + b, &ptr[b * 8])\n"
        "        b = b + 1\n    }\n    return true\n}\n\n"
        "fn save_read(slot_idx: i32, data: *SaveSlot) -> bool {\n"
        "    if not eeprom.present() { return false }\n"
        "    let block_start: i32 = slot_idx * 8\n"
        "    let ptr: *u8 = data as *u8\n    let b: i32 = 0\n"
        "    loop {\n        if b >= 8 { break }\n"
        "        eeprom.read(block_start + b, &ptr[b * 8])\n"
        "        b = b + 1\n    }\n"
        "    -- validate\n"
        "    if data.magic != SAVE_MAGIC { return false }\n"
        "    let stored: u16 = data.checksum\n    data.checksum = 0\n"
        "    let actual: u16 = save_checksum(data)\n"
        "    data.checksum = stored\n    return stored == actual\n}\n```")

